class ArgSort(torch.nn.Module):
    def forward(self, x):
        return torch.argsort(x, dim=-1)


@pytest.mark.nightly
@pytest.mark.single_device
@pytest.mark.record_test_properties(
    category=Category.OP_TEST,
    torch_op_name="torch.argsort",
)
def test_argsort_qwen_vl_window_index():
    """argsort of the EXACT window_index tensor from Qwen2.5-VL-3B (grid [1,38,58]).

    This is the real op the vision tower runs to reverse its window permutation;
    its wrong indices on device scramble the image embeds (full-model PCC ~0.24).
    Tracks https://github.com/tenstorrent/tt-xla/issues/1797.
    """
    x = torch.tensor(QWEN_VL_WINDOW_INDEX, dtype=torch.int64).unsqueeze(0)
    run_op_test(
        ArgSort(),
        [x],
        framework=Framework.TORCH,
    )
